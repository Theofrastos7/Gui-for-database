    private void process_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_process_buttonActionPerformed
        
        try {
        Class.forName("com.mysql.jdbc.Driver");
        sqlConn = DriverManager.getConnection(DB_URL, username, password);
        
        int jobId = Integer.parseInt(job_id_text.getText());
        
        PreparedStatement countStmt = sqlConn.prepareStatement("SELECT COUNT(*) AS job_count FROM applies WHERE job_id = ? AND status = 'Active'");
        countStmt.setInt(1, jobId);
        ResultSet countResult = countStmt.executeQuery();

        int jobCount = 0;
        if (countResult.next()) {
            jobCount = countResult.getInt("job_count");
        }

        count_text.setText(Integer.toString(jobCount));
        
        // Control flow based on jobCount
        if (jobCount == 0) {
            JOptionPane.showMessageDialog(this, "No applications in this job.");
            return; // Stops further execution
        } else {
            // Proceed with your code
            // ...
            pst = sqlConn.prepareCall("{call find_best_applicant(?)}");
            pst.setInt(1, jobId);
            pst.execute();
            
            pst = sqlConn.prepareCall("{call complete(?)}");
            pst.setString(1, job_id_text.getText()); 
            
            pst.setInt(1, jobId);
            pst.execute();
        }
        
        // Other code to execute if jobCount > 0
        upDateDB();
        upDateDB1();
        upDateDB2();
    } catch (ClassNotFoundException ex) {
        java.util.logging.Logger.getLogger(applies_frame.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(applies_frame.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    }
    
    }//GEN-LAST:event_process_buttonActionPerformed

