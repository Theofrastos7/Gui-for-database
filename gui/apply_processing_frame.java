    public void upDateDB()
    {
        try 
        {
            Class.forName("com.mysql.jdbc.Driver");
            sqlConn = DriverManager.getConnection(DB_URL,username,password);
            pst = sqlConn.prepareStatement("Select * FROM applies");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTable3.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next())
            {
                Vector columnData = new Vector();
                
                for (i = 1; i<= q; i++)
                {
                    columnData.add(rs.getString("em_username"));
                    columnData.add(rs.getInt("job_id"));
                    columnData.add(rs.getDate("date_of_submission"));
                    columnData.add(rs.getString("status"));
                    columnData.add(rs.getString("evaluator1"));
                    columnData.add(rs.getInt("score1"));
                    columnData.add(rs.getString("evaluator2"));
                    columnData.add(rs.getInt("score2"));
                    
                }
                
                RecordTable.addRow(columnData);
            }                
        }
        catch(Exception ex) 
        {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    } 
    
